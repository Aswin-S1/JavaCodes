package com.training.variables;
/* Variables --> Container to hold the data in a memory
 * 
 * Local Variables --> Declared inside the method 
 * 
 * Instance Variable --> Declared inside the class & access through object
 * 
 * Static Variable --> Declared static and memory allocated once (Remains same)
 * */
public class VariableTypes {

	public static void main(String[] args) {
		
		int num_1 = 10;
		char val = 'S';
		String nameIs = "Aswin";
		double num = 7.88;
		System.out.println("Name is "+nameIs);
		System.out.println("Number is "+num_1);
		System.out.println("Character is "+val);
		System.out.println("Double is "+num);

	}

}
